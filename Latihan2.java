    private void lstHobiValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstHobiValueChanged
        // TODO add your handling code here:
        Object[] selectedItem = lstHobi.getSelectedValues();
        
        if(selectedItem == null || selectedItem.length == 0){
            txtHobi.setText(null);
        }
        else{
            StringBuilder strValues = new StringBuilder();
            for(Object Item : selectedItem) {
                StringBuilder append = strValues.append(Item.toString()).append(", ");
            }
            txtHobi.setText(strValues.substring(0, strValues.length() -2));
        }
    }//GEN-LAST:event_lstHobiValueChanged

